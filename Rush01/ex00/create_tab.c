#include <unistd.h>

void	write_num(char a, char b, char c, char d);

void	create_tab(char *av[])
{
	write_num(av[1][0], av[1][2], av[1][4], av[1][6]);
	write(1, "\n", 1);
    	write_num(av[1][8], av[1][10], av[1][12], av[1][14]);
	write(1, "\n", 1);    	
    	write_num(av[1][16], av[1][18], av[1][20], av[1][22]);
	write(1, "\n", 1);    	
    	write_num(av[1][24], av[1][26], av[1][28], av[1][30]);
}
